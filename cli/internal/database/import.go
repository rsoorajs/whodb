// ReadCSV reads a CSV file and returns headers and rows.
// If hasHeader is true, the first row is used as column names.
func ReadCSV(filePath string, delimiter rune, hasHeader bool) ([]string, [][]string, error) {
	return ReadFileForImport(filePath, ImportOptions{
		HasHeader: hasHeader,
		Delimiter: delimiter,
	})
}

// ReadExcel reads the first sheet of an Excel file and returns headers and rows.
func ReadExcel(filePath string, hasHeader bool) ([]string, [][]string, error) {
	return ReadFileForImport(filePath, ImportOptions{
		HasHeader: hasHeader,
	})
}

// DetectDelimiter guesses the CSV delimiter by examining the first few lines.
func DetectDelimiter(filePath string) rune {
	data, err := os.ReadFile(filePath)
	if err != nil {
		return ','
	}

	delimiter, err := coreimporter.DetectCSVDelimiter(data)
	if err != nil || len(delimiter) != 1 {
		return ','
	}

	return rune(delimiter[0])
}

// DetectFormat returns "csv" or "excel" based on file extension.
func DetectFormat(filePath string) string {
	if coreimporter.FormatFromPath(filePath) == coreimporter.FileFormatExcel {
		return "excel"
	}
	return "csv"
}

// InferColumnTypes examines sample data rows and returns SQL type names.
func InferColumnTypes(headers []string, rows [][]string) []string {
	types := make([]string, len(headers))
	for idx := range types {
		types[idx] = inferColumnType(idx, rows)
	}
	return types
}

func inferColumnType(colIdx int, rows [][]string) string {
	hasInt, hasFloat, hasBool := true, true, true
	nonEmpty := 0

	limit := len(rows)
	if limit > 100 {
		limit = 100
	}

	for _, row := range rows[:limit] {
		if colIdx >= len(row) || row[colIdx] == "" {
			continue
		}

		val := strings.TrimSpace(row[colIdx])
		nonEmpty++

		if _, err := strconv.ParseInt(val, 10, 64); err != nil {
			hasInt = false
		}
		if _, err := strconv.ParseFloat(val, 64); err != nil {
			hasFloat = false
		}

		lower := strings.ToLower(val)
		if lower != "true" && lower != "false" && lower != "0" && lower != "1" {
			hasBool = false
		}
	}

	if nonEmpty == 0 {
		return "TEXT"
	}
	if hasBool {
		return "BOOLEAN"
	}
	if hasInt {
		return "INTEGER"
	}
	if hasFloat {
		return "REAL"
	}

	return "TEXT"
}

// ImportData imports parsed rows into the database using the manager's current connection.
func (m *Manager) ImportData(schema, tableName string, headers []string, rows [][]string, opts ImportOptions) (*ImportResult, error) {
	start := time.Now()
	defer func() {
		m.logOperation(fmt.Sprintf("ImportData(%s, %d rows)", tableName, len(rows)), start, len(rows), nil)
	}()

	if m.currentConnection == nil {
		return nil, fmt.Errorf("not connected to any database")
	}
	if m.config.GetReadOnly() {
		return nil, ErrReadOnly
	}

	plugin, pluginConfig, err := m.currentPlugin()
	if err != nil {
		return nil, err
	}

	result := &ImportResult{}

	if opts.CreateTable {
		types := InferColumnTypes(headers, rows)
		fields := make([]engine.Record, len(headers))
		for idx, header := range headers {
			fields[idx] = engine.Record{
				Key:   header,
				Value: types[idx],
			}
		}

		created, err := plugin.AddStorageUnit(pluginConfig, schema, tableName, fields)
		if err != nil {
			return nil, fmt.Errorf("create table: %w", err)
		}
		result.TableCreated = created
	}

	targetColumns, err := plugin.GetColumnsForTable(pluginConfig, schema, tableName)
	if err != nil {
		return nil, humanizeImportError(fmt.Errorf("%s", "import.error.table_columns"))
	}
	if err := plugin.MarkGeneratedColumns(pluginConfig, schema, tableName, targetColumns); err != nil {
		// Keep import behavior permissive when generated-column enrichment fails.
	}

	mappings, _, err := coreimporter.BuildDefaultMappings(
		headers,
		targetColumns,
		opts.useHeaderMapping(),
		opts.AllowAutoGenerated,
	)
	if err != nil {
		return nil, humanizeImportError(err)
	}

	importResult, err := coreimporter.Execute(plugin, pluginConfig, &coreimporter.ExecuteRequest{
		Schema:             schema,
		StorageUnit:        tableName,
		Mode:               opts.modeOrDefault(),
		Parsed:             &coreimporter.ParsedFile{Columns: headers, Rows: rows},
		Mapping:            mappings,
		AllowAutoGenerated: opts.AllowAutoGenerated,
		BatchSize:          opts.batchSizeOrDefault(),
		TargetColumns:      targetColumns,
	})
	if err != nil {
		return nil, humanizeImportError(err)
	}

	result.RowsImported = importResult.RowsImported
	m.cache.Clear()

	return result, nil
}

// ReadFileForImport reads a CSV or Excel file and returns headers and rows.
func ReadFileForImport(filePath string, opts ImportOptions) ([]string, [][]string, error) {
	parsed, err := parseImportFile(filePath, opts, 0, true)
	if err != nil {
		return nil, nil, humanizeImportError(err)
	}

	return parsed.Columns, parsed.Rows, nil
}

// PreviewImport reads a file and returns the first N rows for preview.
func PreviewImport(filePath string, opts ImportOptions, maxRows int) ([]string, [][]string, error) {
	parsed, err := parseImportFile(filePath, opts, maxRows, false)
	if err != nil {
		return nil, nil, humanizeImportError(err)
	}

	return parsed.Columns, parsed.Rows, nil
}

func (m *Manager) currentPlugin() (engine.PluginFunctions, *engine.PluginConfig, error) {
	dbType := engine.DatabaseType(m.currentConnection.Type)
	plugin := m.engine.Choose(dbType)
	if plugin == nil {
		return nil, nil, fmt.Errorf("plugin not found")
	}

	credentials := m.buildCredentials(m.currentConnection)
	return plugin, engine.NewPluginConfig(credentials), nil
}

func parseImportFile(filePath string, opts ImportOptions, maxRows int, enforceRowCap bool) (*coreimporter.ParsedFile, error) {
	data, err := coreimporter.ReadFile(filePath, coreimporter.MaxFileSizeBytes)
	if err != nil {
		return nil, err
	}

	return coreimporter.ParseFile(
		data,
		opts.fileOptions(filePath),
		maxRows,
		enforceRowCap,
	)
}

func (opts ImportOptions) fileOptions(filePath string) *coreimporter.FileOptions {
	var delimiter *string
	if opts.Delimiter != 0 {
		value := string(opts.Delimiter)
		delimiter = &value
	}

	var sheet *string
	if opts.Sheet != "" {
		value := opts.Sheet
		sheet = &value
	}

	return &coreimporter.FileOptions{
		Format:    coreimporter.FormatFromPath(filePath),
		Delimiter: delimiter,
		Sheet:     sheet,
		HasHeader: opts.HasHeader,
	}
}

func (opts ImportOptions) useHeaderMapping() bool {
	if opts.UseHeaderMapping != nil {
		return *opts.UseHeaderMapping
	}
	return opts.HasHeader
}

func (opts ImportOptions) modeOrDefault() coreimporter.Mode {
	if opts.Mode == "" {
		return coreimporter.ModeAppend
	}
	return opts.Mode
}

func (opts ImportOptions) batchSizeOrDefault() int {
	if opts.BatchSize <= 0 {
		return 500
	}
	return opts.BatchSize
}

func humanizeImportError(err error) error {
	key := coreimporter.ErrorKeyFromError(err)
	if key == "" {
		return err
	}

	message, ok := importErrorMessages[key]
	if !ok {
		message = importErrorMessages["import.validation.generic"]
	}

	return fmt.Errorf("%s", message)
}
