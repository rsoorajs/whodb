// ImportOptions configures how data is imported.
type ImportOptions struct {
	HasHeader          bool
	Delimiter          rune
	CreateTable        bool
	BatchSize          int
	Mode               coreimporter.Mode
	Sheet              string
	UseHeaderMapping   *bool
	AllowAutoGenerated bool
}

// ImportResult reports the outcome of an import operation.
type ImportResult struct {
	RowsImported int
	TableCreated bool
}

